char	*ft_strrchr(const char *s, int c)
{
	char	*flag;

	flag = 0;
	while (*s)
	{
		if (*s == (char)c)
			flag = (char *)s;
		s++;
	}
	if (c == 0)
		return ((char *)s);
	return (flag);
}
